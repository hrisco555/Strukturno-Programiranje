/*
Даден е правоаголник кој се наоѓа во првиот квадрант од координатниот систем и страните му се паралелни со оските. Правоаголникот се задава со координатите на долното лево и горното десно теме (темињата А и С) кои се цели броеви. Потоа, се внесуваат координатите на две точки. Вашата програма за секоја од двете точки на излез треба да отпечати DA, ако дадената точка лежи на страните на правоаголникот и NE ако е надвор од него или внатре во него.*/

#include <stdio.h>

int main() {
    
    int ax1,ay1,cx2,cy2,x,y,i;
    scanf("%d %d %d %d", &ax1, &ay1, &cx2, &cy2);

    for(i=0;i<2;i++){
        scanf("%d %d", &x,&y);
        if((x >= ax1 && x <= cx2) && (x == ax1 || x==cx2)){
            printf("DA\n");
        }
        else if((y >= ay1 && y <= cy2) && (y == ay1 || y==cy2)){
            printf("DA\n");
        }
        else {
            printf("NE\n");
        }
    }

    return 0;
}
