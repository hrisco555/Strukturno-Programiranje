#include <stdio.h>


void Matrix(double a[100][100],int n, int m){
    
    double min=99999, max=-99999;
    int i,j;
    
    for(j=0;j<m;j++){
        min=a[0][j]; //prvata redica da e 0.00
        max=a[0][j];
        for(i=0;i<n;i++){
            
            if(a[i][j]<min){
                min=a[i][j];
            }
            if(a[i][j]>max){
                max=a[i][j];
            }
        }
        
       for(i=0;i<n;i++){
            a[i][j]=(a[i][j]-min)/(max-min);
        }
        
    }
    
    for(i=0;i<n;i++){
        for(j=0;j<m;j++){
            printf("%.2lf ", a[i][j]);
        }
        printf("\n");
    }
}

int main() {
    
    double a[100][100];
    int n,m;
    scanf("%d%d", &n, &m);
    
    for(int i=0;i<n;i++){
        for(int j=0;j<m;j++){
            scanf("%lf", &a[i][j]);
        }
    }
    
    Matrix(a,n,m);
        
    return 0;
}

