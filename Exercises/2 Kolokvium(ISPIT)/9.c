double Neprekinata_Dropka(int broj[100],int n){
    
    if(n==1){
        return broj[0]; //vraka 0 vo nizata
    }
    
    return broj[0] + 1.0 / Neprekinata_Dropka(broj+1 , n-1);
    
}

